export const Triangle = (x) => {
    1.0 - x;
};

export const Hermite = (x) => {
    return 1.0 - x * x * (3.0 - 2.0 * x);
};

export const BSpline2 = (x) => {
    if (x <= 0.5)
        return 0.75 - x * x;
    const x_ = x - 1.5;
    return 0.5 * x_ * x_;
};

